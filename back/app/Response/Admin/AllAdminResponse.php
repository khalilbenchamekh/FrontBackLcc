<?php

namespace App\Response\Admin;

use Illuminate\Http\Resources\Json\ResourceCollection;

class AllAdminResponse extends ResourceCollection
{
public function toArray($request)
{    //   return parent::toArray($request); // TODO: Change the autogenerated stub

    $collection=$this->collection->toArray();
    return [
        'users'=>array_map(fn($collection)=>new AdminResponse($collection),$collection)
    ];
}
}
